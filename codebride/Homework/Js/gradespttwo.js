var score = 69;
if (score >= 100) {
  console.log('A+');
}
else if (score >= 95) {
  console.log('A');
}
else if (score >= 90) {
  console.log('A-');
}
else if (score >= 86) {
  console.log('B+');
}
else if (score >= 85) {
  console.log('B');
}
else if (score >= 80) {
  console.log('B-');
}
else if (score >= 76) {
  console.log('C+');
}
else if (score >= 70) {
  console.log('C-');
}
else {
  console.log('F');
}











